// ── Helper: make an ID from name ────────────────────────
function makeId(name) {
    return 'char_' + name.toLowerCase().replace(/[^a-z0-9]+/g, '_').replace(/_+$/, '');
}

// ── Character template ──────────────────────────────────
function makeCharacter(name, category, tags, description, state) {
    const id = makeId(name);
    return {
        id,
        type: 'character',
        name,
        tags: [category, ...tags],
        meta: {
            created: '2026-03-31',
            modified: '2026-03-31',
            origin: 'batch',
            origin_ref: category,
            creator: 'batch-generator',
            version: 1,
            thumbnail: null,
        },
        payload: {
            description,
            format: 'character_state',
            state: {
                heightPreset: 'medium',
                widthPreset: 'moderate',
                headHeightPreset: 'medium',
                headWidthPreset: 'moderate',
                faceHeightPreset: 'medium',
                faceWidthPreset: 'moderate',
                headShape: 'roundedBox',
                bodyShape: 'roundedBox',
                facePlacement: 'mid',
                scalpColor: '#4a3728',
                skinColor: SKIN.fair,
                torsoColor: '#666666',
                bottomColor: '#333333',
                eyeIrisColor: null,
                eyePupilSize: null,
                eyeIrisSize: null,
                eyeShape: 'circle',
                lipColor: null,
                lipThickness: null,
                hairStyle: 'none',
                hairColor: '#4a3728',
                hatStyle: 'none',
                hatColor: '#333333',
                facialHairStyle: 'none',
                facialHairColor: '#4a3728',
                glassesStyle: 'none',
                glassesColor: '#333333',
                ...state,
            },
        },
    };
}

